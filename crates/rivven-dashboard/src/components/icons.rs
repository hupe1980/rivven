use leptos::*;

/// Icon size variants
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum IconSize {
    Small,  // 16px
    #[default]
    Medium, // 20px
    Large,  // 24px
}

impl IconSize {
    pub fn class(&self) -> &'static str {
        match self {
            IconSize::Small => "icon icon-sm",
            IconSize::Medium => "icon",
            IconSize::Large => "icon icon-lg",
        }
    }
}

